devided_number:int=int(input("Please enter a number to be devided: "))
devided_by:int=int(input("Please enter a number to be devided by: "))
devided_answer=devided_number//devided_by
remainder_number=devided_number%devided_by
print(f"The result of devided number is {devided_answer} and remainder is {remainder_number}")